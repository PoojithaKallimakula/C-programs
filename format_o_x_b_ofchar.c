#include <stdio.h>

int main() {
    char ch = '9';

    // Display the character '9'
    printf("Character: %c\n", ch);

    // Display the hexadecimal representation of '9'
    printf("Hexadecimal: %x\n", ch);

    // Display the octal representation of '9'
    printf("Octal: %o\n", ch);

    // Display the decimal representation of '9'
    printf("Decimal: %d\n", ch);

    return 0;
}

